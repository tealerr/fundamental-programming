// Integer arithmetic
var myInt1,myInt2: Int
myInt1 = 10
myInt2 = 5

print("####### Integer Arithmetic #######")
print("10 + 5 = ", myInt1+myInt2 )
print("10 - 5 = ", myInt1-myInt2)
print("10 * 5 = ", myInt1*myInt2)
print("10 / 5 = ", myInt1/myInt2)
print("10 % 5 = ", myInt1%myInt2)
print("\n")

// Floating-point arithmetic
var myFloat1,myFloat2: Float

myFloat1 = 5.25
myFloat2 = 3.12

print("####### Float Arithmetic #######")
print("\(myFloat1) + \(myFloat2) =", myFloat1 + myFloat2)
print("\(myFloat1) - \(myFloat2) =", myFloat1 - myFloat2)
print("\(myFloat1) * \(myFloat2) =", myFloat1 * myFloat2)
print("\(myFloat1) / \(myFloat2) =", myFloat1 / myFloat2)
print("\n")

// Mixed arithmetic
var myDouble1, myDouble2: Double
myDouble1 = 12.44
myDouble2 = 3.2

print("####### Double Arithmetic #######")
print("\(myDouble1) + \(myDouble2) =", myDouble1 + myDouble2)
print("\(myDouble1) - \(myDouble2) =", myDouble1 - myDouble2)
print("\(myDouble1) * \(myDouble2) =", myDouble1 * myDouble2)
print("\(myDouble1) / \(myDouble2) =", myDouble1 / myDouble2)
print("\n")

// String concatenation
print("####### String Arithmetic #######")
var myStr1, myStr2: String
myStr1 = "Hello"
myStr2 = "World!"
print("Greeting: ", myStr1+" "+myStr2)
print("\n")
