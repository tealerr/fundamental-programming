(def num1 10)          ; Assign an integer value to num1
(def str1 "Hello")     ; Assign a string value to str1
(def lst1 [1 2 3])     ; Assign a vector to lst1
(def map1 {:a 1 :b 2}) ; Assign a map to map1
(def set1 #{1 2 3})    ; Assign a set to set1
(def nil-value nil)    ; Assign nil (null) to a variable

(println num1)
(println str1)
(println lst1)
(println map1)
(println set1)
(println nil-value)
